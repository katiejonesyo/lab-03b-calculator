export function addNumbers(input1, input2) {
    return Number(input1) + Number(input2) 
}
export function subtractNumbers(input1, input2) {
    return input1 - input2
}
export function multiplyNumbers(input1, input2) {
    return input1 * input2
}
export function divideNumbers(input1, input2) {
    return input1 / input2
}